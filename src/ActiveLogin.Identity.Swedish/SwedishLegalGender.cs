namespace ActiveLogin.Identity.Swedish
{
    /// <summary>
    /// Specifies possible legal genders (juridiskt kön) in sweden.
    /// </summary>
    public enum SwedishLegalGender
    {
        /// <summary>
        /// Unknown or other gender
        /// </summary>
        Other,

        /// <summary>
        /// Gender representing a woman
        /// </summary>
        Woman,

        /// <summary>
        /// Gender representing a man
        /// </summary>
        Man
    }
}