namespace ActiveLogin.Identity.Swedish
{
    /// <summary>
    /// Specifies possible genders (juridiskt kön) in sweden.
    /// </summary>
    public enum SwedishGender
    {
        /// <summary>
        /// Unknown or other gender
        /// </summary>
        Unknown,

        /// <summary>
        /// Gender representing a female
        /// </summary>
        Female,

        /// <summary>
        /// Gender representing a male
        /// </summary>
        Male
    }
}